        [HttpPost, ActionName("Edit")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> EditPost(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }
            var categoryToUpdate = await _context.Categories.FirstOrDefaultAsync(s => s.Id == id);
            if (await TryUpdateModelAsync<Category>(
                categoryToUpdate,
                "",
                s => s.Name))
            {
                try
                {
                    var categories = _context.Categories.Where(c => c.Name == categoryToUpdate.Name).FirstOrDefault();

                    if (categories != null && categories.Id != categoryToUpdate.Id)
                    {
                        ModelState.AddModelError(string.Empty, "This category already exists");
                    }
                    else
                    {
                        await _context.SaveChangesAsync();
                        return RedirectToAction(nameof(Index));
                    }
                }
                catch (DbUpdateException /* ex */)
                {
                    //Log the error (uncomment ex variable name and write a log.)
                    ModelState.AddModelError("", "Unable to save changes. " +
                        "Try again, and if the problem persists, " +
                        "see your system administrator.");
                }
            }
            return View(categoryToUpdate);
        }

        // GET: Categories/Delete/5
        public async Task<IActionResult> Delete(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var category = await _context.Categories
                .FirstOrDefaultAsync(m => m.Id == id);
            if (category == null)
            {
                return NotFound();
            }

            return View(category);
        }

